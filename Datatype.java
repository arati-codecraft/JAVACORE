public class Datatype {
    public static void main(String[] x) {
        int a = 2;
        char ch = 'A'; 
        float f = 3.14f; 
        double d = 2.34637;
        long b = 1234545;

        System.out.println("a = " + a);
        System.out.println("ch = " + ch);
        System.out.println("f = " + f);
        System.out.println("d = " + d);
        System.out.println("b = " + b);
    }
}
